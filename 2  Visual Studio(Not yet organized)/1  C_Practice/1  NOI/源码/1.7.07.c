/*脱氧核糖核酸（DNA）由两条互补的碱基链以双螺旋的方式结合而成。
而构成DNA的碱基共有4种，分别为腺瞟呤（A）、鸟嘌呤（G）、胸腺嘧啶（T）和胞嘧啶（C）。
我们知道，在两条互补碱基链的对应位置上，腺瞟呤总是和胸腺嘧啶配对，鸟嘌呤总是和胞嘧啶配对。
你的任务就是根据一条单链上的碱基序列，给出对应的互补链上的碱基序列。

输入
一个字符串，表示一条碱基链。这个字符串只含有大写字母A、T、G、C，分别表示腺瞟呤、胸腺嘧啶、鸟嘌呤和胞嘧啶。字符串长度不超过255。
输出
一个只含有大写字母A、T、G、C的字符串，为与输入的碱基链互补的碱基链。*/

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define len_max 256

int main(void) {
    char* str_base = (char*)malloc(len_max * sizeof(char));
    
    // 读取输入
    fgets(str_base, len_max, stdin);
    str_base[strcspn(str_base, "\n")] = 0;  // 移除换行符
    size_t len = strlen(str_base);

    // 为互补链分配内存
    char* str_combase = (char*)malloc((len + 1) * sizeof(char)); // +1 用于结尾的 '\0'

    // 生成互补链
    for (size_t i = 0; i < len; i++) {
        switch (str_base[i]) {
            case 'A':
                str_combase[i] = 'T';
                break;
            case 'T':
                str_combase[i] = 'A';
                break;
            case 'G':
                str_combase[i] = 'C';
                break;
            case 'C':
                str_combase[i] = 'G';
                break;
            default:
                str_combase[i] = '0';  // 处理无效字符（可选）
                break;
        }
    }

    str_combase[len] = '\0';  // 添加字符串结束符
    printf("%s\n", str_combase);  // 输出互补链

    // 释放内存
    free(str_base);
    free(str_combase);
    return 0;
}
