/*陶陶家的院子里有一棵苹果树，每到秋天树上就会结出10个苹果。苹果成熟的时候，陶陶就会跑去摘苹果。
陶陶有个30厘米高的板凳，当她不能直接用手摘到苹果的时候，就会踩到板凳上再试试。
现在已知10个苹果到地面的高度，以及陶陶把手伸直的时候能够达到的最大高度
，请帮陶陶算一下她能够摘到的苹果的数目。假设她碰到苹果，苹果就会掉下来。
输入
包括两行数据。第一行包含10个100到200之间（包括100和200）的整数（以厘米为单位）分别表示10个苹果到地面的高度，两个相邻的整数之间用一个空格隔开。
第二行只包括一个100到120之间（包含100和120）的整数（以厘米为单位），表示陶陶把手伸直的时候能够达到的最大高度。
输出
包括一行，这一行只包含一个整数，表示陶陶能够摘到的苹果的数目*/

#include <stdio.h>
#include <stdlib.h>

int main(void) {
    int appleHeights[10]; // 定义一个数组存储苹果的高度
    int taotaoHeight; // 定义陶陶能够达到的最大高度
    int count = 0; // 初始化可摘苹果数量为0

    // 输入10个苹果的高度
    printf("请输入10个苹果的高度（厘米，以空格隔开）：\n");
    for (int i = 0; i < 10; i++) {
        scanf("%d", &appleHeights[i]);
    }

    // 输入陶陶能够达到的最大高度
    printf("请输入陶陶的最大手伸高度（厘米）：\n");
    scanf("%d", &taotaoHeight);
    
    // 计算陶陶能够摘到的苹果数量
    for (int i = 0; i < 10; i++) {
        // 检查陶陶的可摘高度是否大于或等于苹果高度
        if (taotaoHeight + 30 >= appleHeights[i]) {
            count++; // 如果可以摘到，计数加一
        }
    }

    // 输出能够摘到的苹果数量
    printf("%d\n", count);
    return 0;
}
